package day03_sequences_Variables;

public class Print_vs_Println {
    public static void main (String[] args){
        System.out.println("A");     //A+ new Line
        System.out.println("B");    //B+ new line
        System.out.println("C");   // C+ new line  Println appended a new line
        System.out.print("A");     //A no line
        System.out.print("B");    //B no line
        System.out.print("C");   //C no line  Print does not append a new line
    }



}
