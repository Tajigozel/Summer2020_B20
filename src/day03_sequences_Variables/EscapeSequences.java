package day03_sequences_Variables;

public class EscapeSequences {
    public static void main(String [] args){
        System.out.println("Hello everyone \nMy name is Cybertek");// \n gives new  line
        System.out.println("\tI'm in Maryland"); // \t gives a paragraph
        System.out.println("\n\n\t\t\tI love programming"); // \n\n gives 2 new line and 2 indentions
        System.out.println("\\\\"); // in order to print backslash it shout be 2 \\
        System.out.println("//"); // will print all the amount of frontslash
        System.out.println("My name is \'Guzel\'"); // \' gives a single quote, it also works without backslash
        System.out.println("My name is \"Guzel\"");// \" gives double quote
    }

}
