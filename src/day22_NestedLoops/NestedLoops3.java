package day22_NestedLoops;

public class NestedLoops3 {
    public static void main(String[] args) {

        int j = 1;
        while(j <= 4){
            for(int i = 1; i <= 10; i++){
                System.out.print("*");
            }
            System.out.println();

            j++;
        }








    }
}
