package day04;

public class concate_addpractices6 {
    public static void main (String[] args){
        //addition: number + number
        System.out.println(12 + 13); //  25 addition
        System.out.println('A' + 2); // 67 addition
        System.out.println('A' + 'B'); // 131 addition

        //Concatenation: append
        System.out.println("12" + 13); // 1213 concatenation
        System.out.println("A" + 2); // A2 concatenation
        System.out.println("Gender: " + 'M'); //Gender: M concatenation
        System.out.println("Tax: " + 3.5 + "%");//Tax: 3.5% concatenation
                          //"Tax: 3.5" + '%'
        System.out.println(3.5 + '%' + "Tax");//40.5Tax addition & concatenation


    }
}
