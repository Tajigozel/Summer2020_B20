public class variables_practices4char {
    public static void main (String[] args){
        char ch1 = 'A';
        char ch2 = 65;
        System.out.println(ch1);
        System.out.println(ch2);
        char ch5 = 56879;
        System.out.println(ch5);
        char c1 = 'A';  //65
        char c2 = 'B'; //66 =131
        System.out.println(c1 + c2);
        int result = c1 + c2; //131
        System.out.println(result);
        double d = c1 + c2;
        System.out.println(d);
        int num1 = 'A';
        System.out.println(num1);
        char c4 = 65+45;
        System.out.println(c4);
        char c5 = 'Z';
        char c6 = 897;
        System.out.println(c6);
        char c7 = '1'; //the character 1 in ascii table is 49
        System.out.println(c7 + 1); //'1'+1=50
        System.out.println(c7); // char: '1'
        int r1 = c7;
        System.out.println(r1); //int:49
        char charz = 'Z';
        System.out.println(charz);
    }
}
